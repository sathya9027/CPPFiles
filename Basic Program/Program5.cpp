#include <iostream>
#include <string>
//Arithmetic Operator(+,-,*,/,%)
using namespace std;

int main(){
	int a = 23;
	int b = 10;
	cout << "Add : " << a + b << endl;
	cout << "Sub : " << a - b << endl;
	cout << "Mul : " << a * b << endl;
	cout << "Div : " << a / b << endl;
	cout << "Mod : " << a % b << endl;

	return 0;
}