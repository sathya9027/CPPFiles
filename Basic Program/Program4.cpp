#include <iostream>
#include <string>
//Relational Operator(<,>,<=,>=,==,!=)
using namespace std;

int main() {

	int a = 23;
	int b = 10;
	cout << "a=23, b=10" << endl;
	cout << "a<b : " << (a < b) << endl;
	cout << "a>b : " << (a > b) << endl;
	cout << "a<=b : " << (a <= b) << endl;
	cout << "a>=b : " << (a >= b) << endl;
	cout << "a==b : " << (a == b) << endl;
	cout << "a!=b : " << (a != b) << endl;

	return 0;
}